bool areKAnagrams(string str1, string str2, int k){
	vector<int> freq(26, 0);
	int n=str1.length(), m=str2.length();
	for(int i=0; i<n; i++){
		freq[str1[i]-'a']++;
	}
	for(int i=0; i<m; i++){
		freq[str2[i]-'a']--;
	}
	int pos=0;
	for(int i=0; i<26; i++){
		if(freq[i] > 0){pos += freq[i];}
	}
	cout<<n<<" "<<m<<endl;
	return (pos<=k) ? 1 : 0;
}

