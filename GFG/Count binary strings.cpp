ll n, k;
ll dp[1001][1001][2];

ll func(ll i, ll k, bool last){
	//base case
	if(i == n){
		return (k==0);
	}
	//recursive case
	if(dp[i][k][last] != -1){return dp[i][k][last];}

	ll op1 = func(i+1, k, 0);
	ll op2 = 0;
	if(!last || (last && k)){
		op2 = func(i+1, k - (last), 1);
	}
	return dp[i][k][last] = (op1 + op2)%mod;
}

